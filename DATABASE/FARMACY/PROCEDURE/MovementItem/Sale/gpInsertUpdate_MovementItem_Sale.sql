                                            , inGoodsId            := inGoodsId
                                            , inAmount             := inAmount
                                            , inPrice              := inPrice
                                            , inSumm               := outSumm
                                            , inUserId             := vbUserId
                                             );

END;
$BODY$
  LANGUAGE plpgsql VOLATILE;
